package com.example.shubzz.securitycheck_version1;

public class walkClass
{
    private int walk;
    private int counter;

    public walkClass()
    {
    }

    public walkClass(int walk, int counter)
    {
        this.walk = walk;
        this.counter = counter;
    }

    public int getWalk()
    {
        return walk;
    }

    public void setWalk(int walk)
    {
        this.walk = walk;
    }

    public int getCounter()
    {
        return counter;
    }

    public void setCounter(int counter)
    {
        this.counter = counter;
    }
}
